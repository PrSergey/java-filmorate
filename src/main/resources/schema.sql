CREATE TABLE film (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(250) NOT NULL,
    description varchar(200),
    mpa varchar(7),
    release_date timestamp,
    duration integer
);

CREATE TABLE genre (
    id TINYINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(100)
);

CREATE TABLE film_genre (
    id TINYINT REFERENCES genre(id),
    film_id BIGINT REFERENCES film(id)
);

CREATE TABLE person (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email varchar(254) NOT NULL,
    login varchar(30) NOT NULL,
    name varchar(100),
    birthday timestamp
);

CREATE TABLE likes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    film_id BIGINT REFERENCES film(id),
    user_id BIGINT REFERENCES person(id)
);

CREATE TABLE friendship (
    friendship_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES person(id),
    friend_id BIGINT REFERENCES person(id),
    confirmation boolean
);
